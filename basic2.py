# 程序的入口，main函数，增强程序的可读性
def add(num1, num2):
    return num1 + num2

def sub(num1, num2):
    return num1 - num2

if __name__ == '__main__':
    print('如果没有main函数，程序也可以执行，但只能从上往下执行')
    v1 = sub(1, 2)
    v2 = add(3, 4)
    print(v1, v2)

